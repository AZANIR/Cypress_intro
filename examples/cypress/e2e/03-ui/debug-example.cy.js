// ***********************************************************
// Приклад використання debug() для відлагодження тестів
// Цей приклад показує, як використовувати debug() та pause()
// ***********************************************************

describe('Відлагодження тестів з debug()', () => {
  it('приклад використання debug()', () => {
    // Відкриваємо TodoMVC
    cy.visit('/')
    
    // Додаємо задачу
    cy.get('.new-todo').type('Задача для відлагодження{enter}')
    
    // cy.debug() - зупиняє виконання тесту
    // Після цього можна перевірити стан DOM, змінні тощо
    // В Command Log з'явиться кнопка "Resume" для продовження
    cy.debug()
    
    // Після натискання "Resume" виконання продовжиться
    // Перевіряємо, що задача додалася
    cy.get('.todo-list li').should('contain', 'Задача для відлагодження')
  })
  
  it('приклад використання pause()', () => {
    // Відкриваємо TodoMVC
    cy.visit('/')
    
    // cy.pause() - інтерактивна пауза
    // Дозволяє виконувати команди вручну в Command Log
    cy.pause()
    
    // Після паузи можна:
    // - Виконати команди вручну
    // - Перевірити стан сторінки
    // - Подивитися на DOM
    
    // Додаємо задачу після паузи
    cy.get('.new-todo').type('Задача після паузи{enter}')
  })
  
  it('приклад перевірки стану під час виконання', () => {
    cy.visit('/')
    
    // Додаємо кілька задач
    cy.get('.new-todo').type('Задача 1{enter}')
    cy.get('.new-todo').type('Задача 2{enter}')
    cy.get('.new-todo').type('Задача 3{enter}')
    
    // Перевіряємо кількість задач
    // Можна клікнути на команду в Command Log, щоб побачити стан DOM
    cy.get('.todo-list li').should('have.length', 3)
    
    // Відмічаємо першу задачу
    cy.get('.todo-list li').first().find('.toggle').check()
    
    // Перевіряємо стан після зміни
    // У Command Log можна побачити снепшот стану на момент виконання
    cy.get('.todo-list li').first().should('have.class', 'completed')
  })
})

