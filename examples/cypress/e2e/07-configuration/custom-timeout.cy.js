// ***********************************************************
// Налаштування таймаутів
// Цей приклад демонструє різні способи встановлення таймаутів
// ***********************************************************

describe('Налаштування таймаутів', () => {
  it('має використовувати таймаут для окремої команди', () => {
    cy.visit('/')
    
    // Встановлюємо таймаут 10 секунд для цієї команди
    // { timeout: 10000 } - таймаут в мілісекундах (10 секунд)
    // Корисно для повільних елементів або мережевих запитів
    cy.get('.new-todo', { timeout: 10000 }).should('be.visible')
  })
  
  it('має використовувати таймаут для всього тесту', () => {
    // Встановлюємо таймаут для всього тесту
    // Третій параметр it() - об'єкт з опціями
    cy.visit('/')
    
    // Всі команди в цьому тесті матимуть таймаут 10 секунд
    // (якщо не вказано інше)
    cy.get('.new-todo').type('Задача{enter}')
    cy.get('.todo-list li').should('contain', 'Задача')
  })
  
  it('має використовувати різні таймаути для різних команд', () => {
    cy.visit('/')
    
    // Швидка команда - коротший таймаут
    cy.get('.new-todo', { timeout: 2000 }).should('be.visible')
    
    // Повільна операція - довший таймаут
    cy.get('.new-todo', { timeout: 15000 }).type('Задача{enter}')
  })
})

