// ***********************************************************
// Базовий тест для демонстрації Cypress на TodoMVC
// Цей приклад показує найпростіший тест у Cypress
// ***********************************************************

describe('Базовий тест TodoMVC', () => {
  // it() - описує окремий тестовий випадок
  it('має відкрити TodoMVC та відобразити заголовок', () => {
    // cy.visit() - відкриває URL сторінки
    // Оскільки в cypress.config.js встановлено baseUrl,
    // можна використовувати відносний шлях '/'
    cy.visit('/')
    
    // cy.get() - знаходить елемент на сторінці за селектором
    // 'h1' - селектор для заголовка
    // .should() - перевіряє умову
    // 'contain' - перевіряє, що елемент містить текст
    // 'todos' - текст, який має бути в заголовку
    cy.get('h1').should('contain', 'todos')
  })
  
  // Другий тест - перевірка наявності поля вводу
  it('має містити поле для введення нової задачі', () => {
    // Відкриваємо сторінку
    cy.visit('/')
    
    // Шукаємо поле вводу за класом .new-todo
    // .should('be.visible') - перевіряє, що елемент видимий
    cy.get('.new-todo').should('be.visible')
    
    // Перевіряємо, що поле має правильний placeholder
    // .should('have.attr', 'placeholder', ...) - перевіряє атрибут
    cy.get('.new-todo').should('have.attr', 'placeholder', 'What needs to be done?')
  })
})
