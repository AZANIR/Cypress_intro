// ***********************************************************
// Перший тест - демонстрація базового функціоналу Cypress
// Цей тест показує найпростішу перевірку на TodoMVC
// ***********************************************************

describe('Мій перший тест', () => {
  // it() - описує окремий тестовий випадок
  // 'має відкрити TodoMVC' - опис того, що перевіряє тест
  it('має відкрити TodoMVC', () => {
    // cy.visit() - відкриває сторінку
    // '/' - відносний шлях (використовує baseUrl з cypress.config.js)
    // baseUrl встановлено на 'https://todomvc.com/examples/react/dist/'
    cy.visit('/')
    
    // cy.get() - знаходить елемент на сторінці
    // 'h1' - селектор для заголовка (тег h1)
    // .should() - перевіряє умову
    // 'contain' - перевіряє, що елемент містить текст
    // 'todos' - текст, який має бути в заголовку
    cy.get('h1').should('contain', 'todos')
  })
  
  // Другий тест - перевірка наявності основного елемента
  it('має містити поле для введення задач', () => {
    // Відкриваємо сторінку
    cy.visit('/')
    
    // Шукаємо поле вводу за класом .new-todo
    // Це поле для введення нових задач у TodoMVC
    cy.get('.new-todo')
      // .should('be.visible') - перевіряє, що елемент видимий на сторінці
      .should('be.visible')
      // .should('have.attr', ...) - перевіряє наявність атрибута
      // 'placeholder' - назва атрибута
      // 'What needs to be done?' - значення атрибута
      .should('have.attr', 'placeholder', 'What needs to be done?')
  })
})
