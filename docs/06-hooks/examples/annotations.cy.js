// ***********************************************************
// Анотації для керування виконанням тестів
// Цей приклад демонструє використання .only(), .skip(), .timeout()
// ***********************************************************

describe('Анотації для керування тестами', () => {
  // Звичайний тест
  it('звичайний тест - буде виконаний', () => {
    cy.visit('/')
    cy.get('h1').should('contain', 'todos')
  })
  
  // it.only() - виконується ТІЛЬКИ цей тест, інші пропускаються
  // ⚠️ УВАГА: Завжди видаляйте .only() перед комітом!
  it.only('тільки цей тест буде виконаний', () => {
    cy.visit('/')
    cy.get('.new-todo').type('Тест з .only(){enter}')
    cy.get('.todo-list li').should('contain', 'Тест з .only()')
  })
  
  // it.skip() - цей тест буде пропущений
  it.skip('цей тест буде пропущений', () => {
    cy.visit('/')
    // Цей код не виконається
  })
  
  // describe.only() - виконується тільки цей блок
  describe.only('тільки цей блок буде виконаний', () => {
    it('тест в блоці з .only()', () => {
      cy.visit('/')
      cy.get('.new-todo').should('be.visible')
    })
  })
  
  // describe.skip() - весь блок буде пропущений
  describe.skip('цей блок буде пропущений', () => {
    it('тест в пропущеному блоці', () => {
      // Цей код не виконається
    })
  })
})

describe('Використання .timeout()', () => {
  it('тест з встановленням таймауту для команди', () => {
    cy.visit('/')
    
    // Встановлюємо таймаут 10 секунд для цієї команди
    // { timeout: 10000 } - таймаут в мілісекундах
    cy.get('.new-todo', { timeout: 10000 }).should('be.visible')
  })
  
  it('тест з таймаутом для всього тесту', () => {
    // Встановлюємо таймаут для всього тесту
    // Третій параметр it() - опції тесту
    cy.visit('/')
    
    // Всі команди в цьому тесті матимуть таймаут 10 секунд
    cy.get('.new-todo').type('Задача{enter}')
  })
})

